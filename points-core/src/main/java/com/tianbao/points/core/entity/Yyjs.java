package com.tianbao.points.core.entity;

import java.util.Date;

public class Yyjs {
    private Integer id;

    private Integer pid;

    private Date jsdate;

    private Double radios;

    private Integer wy;

    private Integer kg;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getPid() {
        return pid;
    }

    public void setPid(Integer pid) {
        this.pid = pid;
    }

    public Date getJsdate() {
        return jsdate;
    }

    public void setJsdate(Date jsdate) {
        this.jsdate = jsdate;
    }

    public Double getRadios() {
        return radios;
    }

    public void setRadios(Double radios) {
        this.radios = radios;
    }

    public Integer getWy() {
        return wy;
    }

    public void setWy(Integer wy) {
        this.wy = wy;
    }

    public Integer getKg() {
        return kg;
    }

    public void setKg(Integer kg) {
        this.kg = kg;
    }
}